#pragma once

#include <stdexcept>
#include <string>


/* -------------------------------------------------------------------------- */

namespace flox {

/* -------------------------------------------------------------------------- */

class FloxException : public std::exception {
  private:
    std::string msg;
  public:
    FloxException( std::string_view msg ) : msg( msg ) {}
    const char * what() const noexcept { return this->msg.c_str(); }
};


/* -------------------------------------------------------------------------- */

}  /* End Namespace `flox' */


